			<div class="event-about block">
			    <p>
            The Line follower robot is a mobile machine that can detect and follow the line drawn on the floor. Generally, the path is predefined and can be visible like a black line on a white surface with a high contrasted color. The robot has to follow the track with his sensing device and just have complete the map .
          </p>
			</div>
			<div class="block">
			    <h2>Dimensions and fabrication</h2>
			    <ol class="rules">
				<li>The machine should fit in a square of size 250x250 mm. There is no height limit.
				</li><li>The autonomous robot must be stable and must stand on its own at the beginning of the run when put in the starting point. Robots not fulfilling this criterion will be disqualified.
				</li><li>During the run, the autonomous robot can expand itself provided it does not damage the arena in anyway. However, the machine should not leave anything behind while tracing the path. The machine should not scratch, damage or destroy the track or accompanying parts of arena.
				</li><li>All bots found damaging the arena will be immediately disqualified. The final decision is at the discretion of the organizers.
				</li><li>The teams are allowed to use ready- made micro-controller boards/ready-made sensor kits. However they are not allowed to use Lego kits or any similar assemblies.
				</li>			    </ol>
			</div>
			<div class="block">
			    <h2>Power supply and propulsion</h2>
			    <ol class="rules">
				<li>Machine must be completely self-contained and should receive no outside assistance. It should not use an energy source employing a combustion process.
				</li><li>Machine should have an on-board power supply. No external power supply is allowed.
				</li><li>Voltage on the machine at any point should not exceed 15V DC.
				</li>
			    </ol>
			</div>
			<div class="block">
			    <h2>Rounds</h2>
			    <p>Participant must complete two rounds to win the competition. Each round consists of three checkpoints.</p>
			    <h3>Elimination Round</h3>
			    <p>In this round you have to complete the mentioned track with less time and more points. The track is same for all participants.</p>
			    <ol class="rules">
				<li>An elimination round will be held prior to ‘achieving the goal round’.
				</li><li>An elimination round will consists only passing the check points and tracing the lines.
				</li><li>The teams will be ranked according to their points and time.
				</li><li>Top teams will qualify for the finals.
				</li>
			    </ol>
			    <h3>Final Round (achieving the goal)</h3>
			    <p>In this round, first you have to complete 1st round and then you have to start with 2nd round.</p>
			    <ol class="rules">
				<li>It will be consist of two paths, one is easy and the other one is difficult path.
				</li><li>You can choose your own path to complete the round.
				</li><li>As per the path concerned, the difficult path will lead more points as compared with easy one.
				</li><li>Ranking will decide as per the points and time required to complete the path.
				</li>
			    </ol>
			    <div align="center"><img class="img-thumbnail" src="images/track.JPG"><p class="center">Note.: The width of the black line is 3cm</div>
			</div>
			<div class="block">
			    <h2>Rules</h2>
			    <h3>Competition Rules</h3>
			    <ol class="rules">
				<li>Each contesting team will be given 1 minute to make any adjustments to the sensors on test arena.
				</li><li>Once match is started only one member of each team i.e. the operator will be allowed to enter the arena after permission from organizers.
				</li><li>The match shall start from the start point or check point. The bot will start in only one direction as specified by the organizers. It cannot start in either direction. The operator may abort run at any time.
				</li>
			    </ol>
			    <h3>Hand Touch</h3>
			    <ol class="rules">
				<li>If an operator touches the robot during run, this will be considered as a hand-touch and the robot must be put back at the check point crossed or at start point.
				</li><li>The operator can take any number of hand touches. There is NO penalty for first two hand touch but there will be deduction of points per handtouch after two handtouches.
				</li><li>Even if a participant takes a hand touch, the clock will keep running and will not be reset as the robot starts again from the previous restart point.
				</li><li>If a robot gets into trouble the operator can ask the judge for permission to take a hand touch and restart the robot at last check point.
				</li>
			    </ol>
			    <h3>General Rules</h3>
			    <ol class="rules">
				<li>Any team that is not ready at the time specified will be disqualified from the competition automatically.
				</li><li>The bot will be checked for its safety before the race and will be discarded if found unsafe for other participants and spectators.
				</li><li>Participants are not allowed to keep anything inside the arena other than its autonomous robot.
				</li><li>Laptops/personal computers are not allowed near the arena. Other Wi-Fi, Bluetooth, etc. devices must be switched off. The organizers hold the right to check for these devices and their usage.
				</li><li>The score and time measured by the organizers will be final and will be used for scoring the teams. Time measured by any contestant by any other means is not acceptable for scoring.
				</li><li>Operator must wear socks.
				</li><li>In case of any disputes/discrepancies, the organizers’ decision will be final and binding. The organizers reserve the rights to change any or all of the above rules as they deem fit. Change in rules, if any will be highlighted on the website and notified to the registered teams.
				</li><li>A team may consist of members from different educational institutes.
        </li>
			    </ol>
