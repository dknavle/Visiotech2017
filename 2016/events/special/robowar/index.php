			<div class="block">
			    <h2>Dimensions and Fabrications</h2>
			    <ol class="rules">

				<li>
				    The robot should fit in the box of 750 mm x 750 mm x 750 mm (l x b x h). The external device used to control the robot or any external tank is not included in the size constraint.
				</li><li>The robot should not exceed 40 kg of weight including the weight of pneumatic source/tank. If the tank is external, its weight would be considered 1.5 times its actual weight.
				</li><li>Weight of robots having an on-board power supply will be counted as 0.6 times the actual weight.
				</li><li>Weight of adaptors and the remote controller will not be counted.
				</li>

			    </ol>
			</div>






			<div class="block">
			    <h2>Motion of Robot</h2>
			    <ol class="rules">

				<li>
				    Jumping and hopping is not allowed.
				</li><li>Flying (using airfoil, helium balloons, ornithopters etc.) is not allowed.
				</li>

			    </ol>
			</div>

			<div class="block">
			    <h2>Control of Robot</h2>
			    <ol class="rules">

				<li>Robot can have a wired or wireless control.
				</li><li>Off board power supplies are allowed.
				</li><li>If the machine is wired then the wire should remain slack under all circumstances during the competition. All the wires and pneumatic hoses coming out of the machine should be stacked as a single unit. The wires should be properly insulated. Minimum length of wire should be 5 meter.
				</li><li>In case of wireless control the case of frequency interference with other teams will not be considered for rematch or for result.
				</li><li>The control wires should be drawn 1 meter above the ground.
				</li>
			    </ol>
			</div>

			<div class="block">
			    <h2>Battery and Power</h2>
			    <ol class="rules">

				<li>The machine can be powered electrically only. Use of an IC engine in any form is not allowed. On board Batteries must be sealed, immobilized- electrolyte types (such as gel cells, lithium, NiCad, NiMH, or dry cells).
				</li><li>The electric voltage between 2 points anywhere in the machine should not be more than 36 V DC at any point of time. If a team is using AC voltage in any of its parts then the voltage should not exceed 36 V AC at any point of time as well.
				</li><li>All efforts must be made to protect battery terminals from a direct short and causing a battery fire, failure to do so will cause direct disqualification.
				</li><li>Use of damaged, non-leak proof batteries may lead to disqualification.
				</li><li>Special care should be taken to protect the on-board batteries
				</li><li>Change of battery will not be allowed during the match.
				</li>
			    </ol>
			</div>




			<div class="block">
			    <h2>Hydraulics</h2>
			    <ol class="rules">
				<li>Robot can use non-inflammable liquid to actuate hydraulic devices.
				</li><li>All hydraulic components on-board a robot must be securely mounted.
				</li><li>Special care must be taken while mounting pump, accumulator and armor to ensure that if ruptured, direct fluid streams will not escape the robot.
				</li><li>Whole hydraulic systems should be leak proof. Maximum allowed pressure is 8 bars.
				</li><li>Participant must be able to indicate the used pressure with integrated or temporarily fitted pressure gauge.
				</li>
			    </ol>
			</div>






			<div class="block">
			    <h2>Pneumatics</h2>
			    <ol class="rules">

				<li>Robot can use pressurized non-inflammable gases to actuate pneumatic devices. Maximum allowed pressure is 8 bars. The storage tank and pressure regulators used by teams need to be certified. Teams using pneumatics are required to produce the Safety and Security letters at the registration desk at the venue. Failing to do so will lead to direct disqualification.
				</li><li>Participants must be able to indicate the used pressure with integrated or temporarily fitted pressure gauge. Also there should be provision to check the cylinder pressure on the bot.
				</li><li>You must have a safe way of refilling the system and determining the on board pressure. All pneumatic components on board a robot must be securely mounted. Care must be taken while mounting the pressure vessel and armor to ensure that if ruptured it will not escape the robot. Failing to do so will lead to direct disqualification.
				</li>
			    </ol>
			</div>








			<div class="block">
			    <h2>Weapons</h2>
			    <p>
				Robots can have any kind of cutters, flippers, saws, lifting devices, spinning hammers etc. as weapons</p>
			    <h3>Exceptions and Limitations for weapons</h3>
			    <ol class="rules">

				<li>Liquid projectiles.
				</li><li>Any kind of inflammable liquid.
				</li><li>Flame-based weapons.
				</li><li>Any kind of explosive or intentionally ignited solid or potentially ignitable solid.
				</li><li>Nets, tape, glue, chains or any other entanglement device.
				</li><li>High power magnets or electromagnets.
				</li><li>Radio jamming, lasers, tesla coils, or any other high-voltage device.
				</li><li>Tethered or un-tethered projectiles.
				</li><li>Spinning weapons which do not come in contact with the arena at any point of time are allowed. In any case the arena should not be damaged by any bot.
				</li>
			    </ol>
			</div>









			<div class="block">
			    <h2>Safety Rules</h2>
			    <ol class="rules">


				<li>Special care should be taken to protect the on-board batteries and pneumatics, robot without proper protection will not be allowed to compete.
				</li><li>Robots must be activated in the arena or with expressed consent of the event coordinators.
				</li><li>All weapons must have a safety cover on any sharp edges.
				</li><li>All participants build and operate robots at their own risk. Please take care to not hurt yourself or others when building, testing and competing. Organizers will not be responsible for any injuries to operator or team members.
				</li>
			    </ol>
			</div>







			<div class="block">
			    <h2>Instructions</h2>
			    <ol class="rules">




				<li>Participants have to build a single robot.
				</li><li>Robots will be tested for dimensions & safety before each match.
				</li><li>Violation of rules will lead to disqualification of the respective team.
				</li><li>The competition will be played on a knock-out basis.
				</li><li>Each match will be of duration 5 minutes and winner will be decided on the basis of winning criteria.
				</li><li>Judges' decision will be final decision.
				</li>

			    </ol>
			</div>










			<div class="block">
			    <h2>Criteria for Victory</h2>
			    <ol class="rules">

				<li>A robot is declared victorious if its opponent is immobilized.
				</li><li>A robot will be declared immobile if it cannot show linear or rotational motion of at least one foot in a time period of 30 seconds. A robot with one side of its drive train disabled will not be counted immobilized, if it can demonstrate some degree of controlled movement. In case both the robots remain mobile after the end of the round then the winner will be decided subjectively.
				</li><li>A robot that is deemed unsafe by the judges after the match has begun will be disqualified and therefore declared the loser. The match will be immediately halted and the opponent will be awarded a win.
				</li><li>Judges’ and Event Heads’ decision will be treated as final and binding to all.
				</li><li>The organizers reserve the rights to change any or all of the above rules as they deem fit. Keep checking the visiotech website (www.visiotech.in)
				</li><li>In case of a match resulting in a tie, rematch of 2 minutes will be played. If further tie occurs team scoring highest points in first 3 minutes of the main match will be declared as a winner.
				</li><li>Points will be given on the basis of aggression, damage, control and strategy.</li>

				<h3>Aggression</h3>
				<p>Aggression is judged by the frequency, severity, boldness and effectiveness of attacks deliberately initiated by the robot against its opponent. If a robot appears to have accidentally attacked an opponent, that act will not be considered Aggression.
				</p>




				<h3>Control</h3><p>Control means a robot is able to attack an opponent at its weakest point, use its weapons in the most effective way, avoid Arena Hazards, and minimize the damage caused by the opponent or its weapons.</p>
				<h3>Damage</h3>
				<p>Through deliberate action, a robot either directly, or indirectly using the Arena Hazards, reduces the functionality, effectiveness or defensibility of an opponent. Damage is not considered relevant if a robot inadvertently harms itself. Also, if a pressure vessel or a rapidly spinning device on a robot fragments, any damage to the opponent will not be considered "deliberate".
				</p>


				<h3>Strategy</h3>
				<p>The robot exhibits a combat plan that exploits the robot's strengths against the weaknesses of its opponent. Strategy is also defined as a robot exhibiting a deliberate defense plan that guards its weaknesses against the strengths of the opponent. Strategy can also involve using the Arena Hazards to gain an advantage.
				</p>



			    </ol>
