			    <p>“Come Forward and show your abilities of Leadership & Governance.”</p>
                            <p>With a view to strengthening the roots of democracy, inculcate healthy habits of discipline, tolerance of the views of others and to enable the student community to understand the working of our Parliamentary institutions.</p>
                            <p>At <b><i>Visiotech 2016</i></b> we are giving an open opportunity to all the students from technical institutions to represent their ideas about the government policies and let them know the working of our parliament. </p>
                            <p>Youth parliament is an initiative from the “MINISTRY OF PARLIAMENTARY AFFAIRS, Govt. OF India.”  With a view to strengthening the roots of democracy, inculcate healthy habits of discipline, tolerance of the views of others and to enable the student community to understand the working of our Parliamentary institutions, the Ministry of Parliamentary Affairs has introduced the Scheme of National Youth Parliament Competition for Universities/Colleges throughout the country. </p>
                            <p>At MGM’s College of Engineering’s <b><i>Visio-Tech 2016</i></b> we are organizing a prototype event as a competition among the participants.</p>
                            <p>The matters raised in the ‘Youth Parliament session’ may relate to subject of welfare activities, <b><i>defense, social reforms</i></b>, economic development, communal harmony, public health, <b><i>education, internal security, use of modern technology</i></b> etc. Special emphasis may lay on issues relating to human rights, <b>health and education</b>. </p>
                            
