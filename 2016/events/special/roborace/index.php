			<div class="block">
	    <h2>Machine Specification</h2>
	    <ol class="rules">
				<li>The machine should fit in a box of dimensions 250mm X 250mm X 200mm.</li>
				<li>The machine should be controlled by a wireless remote control mechanism or wired one throughout the race.</li>
				<li>The weight of the robot should not exceed 3 Kg's.</li>
				<li>If the machine is wired then the wire should remain slack under all circumstances during the competition. All the wires coming out of the machine should be stacked as single unit .The wires should be properly insulated.</li>
	    </ol>
			</div>

			<div class="block">
			    <h2>Batteries & Power:</h2>
          <ol class="rules">
            <li>Batteries must be sealed, immobilized electrolyte types (gel cells, lithium, NiCad, or dry cells).</li>
            <li>The electric voltage anywhere in the machine should not be more than 15V DC at any point of time.</li>
            <li>On board power supply of 15V DC will be provided.</li>
          </ol>

          <h2>Arena</h2>
            <ol class="rules">
              <li>The arena will consists of slopes, slits, rotating platform, sloppy surface, muddy, water showers, rollers, sharp turnings, narrow bridge etc.</li>
              <li>Exact track will opened on the event day, as it is subjected to change & will be informed to participants before commencement of the event.</li>
            </ol>

            <h1>Rules and Regulation</h1>
            <h2>Game Rules:</h2>
            <ol class="rules">
              <li>The competition is based on time trial system. There will be a qualifying round for each team.</li>
              <li>The top teams from qualifying rounds make it to the final round on basis of time trials.</li>
              <li>Three hand touches and 2 skips are allowed are allowed for final round with penalty of 15secs. and of 45secs respectively.</li>
              <li>The machine will have to follow the provided track.</li>
              <li>If any of the machines starts off before startup call, the counter would be restarted and the machines will get a second chance. However, if any machine starts off before the startup call for a second time, it will be disqualified.</li>
              <li>The track will have check points at regular intervals. If a machine tumbles, or halts, or goes off the arena at any point on the track, one of the participants is allowed to lift it up and place it at the nearest checkpoint behind that point. The time shall still  be running in the meantime.</li>
              <li>One operator will operate only machine in the event.</li>
              <li>Judges decision shall be treated as final and binding on all.</li>
            </ol>

			    <h2>General Rules:</h2>
			    <ol class="rules">
				        <li>Any team that is not ready at the time specified will be eliminated from the competition automatically.</li>
                <li>The teams must adhere to the spirit of healthy competition.</li>
			    </ol>

          <h2>Judging Criteria</h2>
          <ol class="rules">
