			    <h2>Event Structure</h2>			
			    <h3>Round 1</h3><p>(MIND-BENDER): 
                                                Round1 will be an Aptitudical round, where each individual will get a random 20 question which will be on basic DBMS, each correct answer will get +3 marks and wrong answer will result in -1 mark for each question. 
</p>
			    <h3>Round 2</h3><p>(REARRANGING TABLES): 
                                                In this Round the top 30 individuals of Round I will be participating, in this Round each individual will be teamed up with other two individuals (making total of 10 teams), 
                                                the grouping of individuals will be random and computerized.Now each team will be given a table with all the data types and values of records in mixed and messed up format, 
                                                so the teams will be required to identify the data type of each particular attribute and rearrange all the records in right order, top 5 teams will be advanced to the next round i.e. 
                                                ROUND III (Part I), and the top team will directly advance to the ROUND III (Part II). 
</p>
                            <h3>Round 3</h3><p>(ER QUEST): 
                                                The bottom 4 teams of the ROUND II will be participating in this Round, each team will be provided with the 3-ER diagrams which will be erroneous, 
                                                the team is required to correct the ER diagrams and draw them.In this round the bottom two teams will be eliminated. 
</p>
                            <h3>Round4</h3><p>(CRACK THE QUERY): 
                                              Final Round time to pull up your socks, in this round teams will be given queries along with the relations which they need to solve in MYSQL. 
                                              These queries have to be solved by the team in limited period of 1 hour at the end of 1 hour the team which will solve the most queries will be selected as winner and runner up respectively, 
                                              in case of tie the team which solved the queries in least time will be the winner.

